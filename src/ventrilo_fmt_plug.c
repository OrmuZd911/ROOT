#if ARCH_LITTLE_ENDIAN
#define john_htonl(x) ((((x)>>24) & 0xffL) | (((x)>>8) & 0xff00L) | \
                (((x)<<8) & 0xff0000L) | (((x)<<24) & 0xff000000L))
#else
#define john_htonl(x) (x)
#endif


void ventrilo_pwd_hash(unsigned char *pwd, unsigned char *out) {
	unsigned int    crc;
	int             i,
			j,
			edi,
			cnt,
			len,
			chr;

	union {
		unsigned char c[4 + 1];
		ARCH_WORD dummy;
	} buf;
	unsigned char *tmp = buf.c;
	unsigned char *lptr;

	len = strlen((const char*)pwd);
	buf.dummy = 0;
	lptr = out;

	edi = 1;
	cnt = len;
	for(i = 0; i < 32; i++) {
		if (i < len) {
			chr = *pwd++;
		} else {
			chr = ((tmp[(cnt + 1) & 3] + *lptr) - 0x3f) & 0x7f;
			lptr++;
		}

		out[i] = chr;
		crc = 0;
		for (j = 0; j < edi; j++) {
			crc = crc_table[out[j] ^ (crc & 0xff)] ^ (crc >> 8);
		}
		*(unsigned int *)tmp = john_htonl(crc);

		cnt += chr;
		if (crc) {
			while (!tmp[cnt & 3]) cnt++;
		}

		out[i] += tmp[cnt & 3];
		cnt++;
		edi++;
	}
}

static void init(struct fmt_main *self)
{
#ifdef _OPENMP
	static int omp_t = 1;
	omp_t = omp_get_max_threads();
	self->params.min_keys_per_crypt *= omp_t;
	omp_t *= OMP_SCALE;
	self->params.max_keys_per_crypt *= omp_t;
#endif
	saved_key = mem_calloc(self->params.max_keys_per_crypt,
			sizeof(*saved_key));
	crypt_out = mem_calloc(self->params.max_keys_per_crypt,
			sizeof(*crypt_out));
}

static void done(void)
{
	MEM_FREE(crypt_out);
	MEM_FREE(saved_key);
}

static int valid(char *ciphertext, struct fmt_main *self)
{
	char *p = ciphertext;
	if (!strncmp(ciphertext, FORMAT_TAG, TAG_LENGTH))
		p = ciphertext + TAG_LENGTH;

	if(!p)
		return 0;
	if (!ishex(p))
		return 0;

	if (strlen(p) != BINARY_SIZE * 2)
		return 0;

	return 1;
}

static void *get_binary(char *ciphertext)
{
	static union {
		unsigned char c[BINARY_SIZE+1];
		ARCH_WORD dummy;
	} buf;
	unsigned char *out = buf.c;
	char *p = ciphertext;
	int i;

	if (!strncmp(ciphertext, FORMAT_TAG, TAG_LENGTH))
		p = ciphertext + TAG_LENGTH;

	for (i = 0; i < BINARY_SIZE; i++) {
		out[i] =
			(atoi16[ARCH_INDEX(*p)] << 4) |
			atoi16[ARCH_INDEX(p[1])];
		p += 2;
	}

	return out;
}

